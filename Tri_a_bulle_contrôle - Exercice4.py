#4) Le tri à bulles est considéré comme très lent. Pourquoi ?
#Peut-on avoir une idée du temps nécessaire à son exécution, estimer son ordre de grandeur ?
#Détaillez votre réponse.

'''
Le tri à bulle nécessite de parcourir la liste, même diminuée, autant de fois qu'elle contient d'éléments.
A chaque élément, elle le compare avec un autre. C'est pour cela que le programme est lent.
On peut avoir une idée du temps nécessaire à son éxécution, estimer son ordre de grandeur, mais seulement avec
une liste précise. En effet, le temps va augmenter selon la longueur de la liste.
'''